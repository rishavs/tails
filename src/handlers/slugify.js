export const getRandomSlug = () => {

    let randomStartingWord = Math.random() > 0.5 ? adverbs[Math.floor(Math.random() * adverbs.length)] : adjectives[Math.floor(Math.random() * adjectives.length)]

    let randomAdjective = adjectives[Math.floor(Math.random() * adjectives.length)]

    // choose any item randomly from either animals or plants lists
    let randomNoun = Math.random() > 0.5 ? animals[Math.floor(Math.random() * animals.length)] : plants[Math.floor(Math.random() * plants.length)]

    return `${randomStartingWord}-${randomAdjective}-${randomNoun}`
}